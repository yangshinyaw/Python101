# conditionl expressions

# Sample 1
age = 14

result = "Legal age" if age > 18 else "Minor"

print(result)


# Sample 2
temp = 33

weather = "Hot" if temp > 30 else "Cold"

print(weather)

# Sample 3
num = 8

odd_even = "EVEN" if num % 2 == 1 else "ODD"

print(odd_even)

# Sample 4
user_role = "member"

access = "Access granted" if user_role == "admin" else "Access denied"

print(access)
